class SOARActionExecutor:
    """Base class for SOAR action executors"""
    
    async def execute(self, action: SOARAction, context: Dict[str, Any]) -> Dict[str, Any]:
        """Execute the action"""
        raise NotImplementedError

class BlockIPAction(SOARActionExecutor):
    """Block IP address action"""
    
    async def execute(self, action: SOARAction, context: Dict[str, Any]) -> Dict[str, Any]:
        try:
            ip_address = context.get("source_ip")
            if not ip_address:
                return {"status": "failed", "error": "No IP address provided"}
            
            # Execute firewall command (example)
            config = action.config or {}
            firewall_command = config.get("command", f"iptables -A INPUT -s {ip_address} -j DROP")
            
            result = subprocess.run(
                firewall_command.split(),
                capture_output=True,
                text=True,
                timeout=30
            )
            
            if result.returncode == 0:
                return {
                    "status": "success",
                    "message": f"Successfully blocked IP {ip_address}",
                    "command": firewall_command
                }
            else:
                return {
                    "status": "failed",
                    "error": result.stderr,
                    "command": firewall_command
                }
        
        except subprocess.TimeoutExpired:
            return {"status": "failed", "error": "Command timeout"}
        except Exception as e:
            return {"status": "failed", "error": str(e)}

class NotifyAction(SOARActionExecutor):
    """Send notification action"""
    
    async def execute(self, action: SOARAction, context: Dict[str, Any]) -> Dict[str, Any]:
        try:
            config = action.config or {}
            notification_type = config.get("type", "email")
            
            if notification_type == "email":
                return await self._send_email_notification(action, context)
            elif notification_type == "slack":
                return await self._send_slack_notification(action, context)
            elif notification_type == "webhook":
                return await self._send_webhook_notification(action, context)
            else:
                return {"status": "failed", "error": f"Unknown notification type: {notification_type}"}
        
        except Exception as e:
            return {"status": "failed", "error": str(e)}
    
    async def _send_email_notification(self, action: SOARAction, context: Dict[str, Any]) -> Dict[str, Any]:
        config = action.config or {}
        
        # Create email
        msg = MimeMultipart()
        msg['From'] = config.get("from_email", "security@sentinelx.com")
        msg['To'] = config.get("to_email", "admin@sentinelx.com")
        msg['Subject'] = f"Security Alert: {context.get('title', 'SOAR Alert')}"
        
        # Email body
        body = f"""
        Security Alert Details:
        
        Title: {context.get('title', 'N/A')}
        Description: {context.get('description', 'N/A')}
        Severity: {context.get('severity', 'N/A')}
        Source IP: {context.get('source_ip', 'N/A')}
        Timestamp: {context.get('timestamp', 'N/A')}
        
        This alert was automatically generated by SentinelX SOAR engine.
        """
        
        msg.attach(MimeText(body, 'plain'))
        
        # Send email (example using SMTP)
        smtp_config = config.get("smtp", {})
        server = smtplib.SMTP(smtp_config.get("host", "localhost"), smtp_config.get("port", 587))
        server.starttls()
        server.login(smtp_config.get("username"), smtp_config.get("password"))
        
        text = msg.as_string()
        server.sendmail(msg['From'], msg['To'], text)
        server.quit()
        
        return {
            "status": "success",
            "message": "Email notification sent successfully",
            "recipient": msg['To']
        }
    
    async def _send_slack_notification(self, action: SOARAction, context: Dict[str, Any]) -> Dict[str, Any]:
        config = action.config or {}
        webhook_url = config.get("webhook_url")
        
        if not webhook_url:
            return {"status": "failed", "error": "Slack webhook URL not configured"}
        
        # Create Slack message
        slack_message = {
            "text": f"🚨 Security Alert: {context.get('title', 'SOAR Alert')}",
            "attachments": [
                {
                    "color": self._get_slack_color(context.get('severity')),
                    "fields": [
                        {"title": "Description", "value": context.get('description', 'N/A'), "short": False},
                        {"title": "Severity", "value": context.get('severity', 'N/A'), "short": True},
                        {"title": "Source IP", "value": context.get('source_ip', 'N/A'), "short": True},
                        {"title": "Timestamp", "value": context.get('timestamp', 'N/A'), "short": True}
                    ]
                }
            ]
        }
        
        # Send to Slack
        async with httpx.AsyncClient() as client:
            response = await client.post(webhook_url, json=slack_message, timeout=30)
            
            if response.status_code == 200:
                return {
                    "status": "success",
                    "message": "Slack notification sent successfully"
                }
            else:
                return {
                    "status": "failed",
                    "error": f"Slack API error: {response.status_code} - {response.text}"
                }
    
    async def _send_webhook_notification(self, action: SOARAction, context: Dict[str, Any]) -> Dict[str, Any]:
        config = action.config or {}
        webhook_url = config.get("url")
        
        if not webhook_url:
            return {"status": "failed", "error": "Webhook URL not configured"}
        
        # Create webhook payload
        payload = {
            "alert": context,
            "timestamp": datetime.utcnow().isoformat(),
            "source": "sentinelx-soar"
        }
        
        # Send webhook
        async with httpx.AsyncClient() as client:
            response = await client.post(
                webhook_url, 
                json=payload, 
                timeout=30,
                headers=config.get("headers", {})
            )
            
            if response.status_code in [200, 201, 202]:
                return {
                    "status": "success",
                    "message": "Webhook notification sent successfully",
                    "response_code": response.status_code
                }
            else:
                return {
                    "status": "failed",
                    "error": f"Webhook error: {response.status_code} - {response.text}"
                }
    
    def _get_slack_color(self, severity: str) -> str:
        """Get Slack color based on severity"""
        colors = {
            "critical": "danger",
            "high": "warning",
            "medium": "warning",
            "low": "good"
        }
        return colors.get(severity.lower(), "warning")


class SOAREngine:
    """Main SOAR engine"""
    
    def __init__(self):
        self.is_running = False
        self.action_executors = {
            ActionType.BLOCK_IP: BlockIPAction(),
            ActionType.NOTIFY: NotifyAction(),
            ActionType.DISABLE_USER: DisableUserAction(),
        }
        self.running_executions = {}
        self.max_concurrent = settings.SOAR_MAX_CONCURRENT_EXECUTIONS
    
    async def start(self):
        """Start the SOAR engine"""
        if self.is_running:
            logger.warning("SOAR engine is already running")
            return
        
        self.is_running = True
        logger.info("SOAR engine started")
        
        # Start background processing
        asyncio.create_task(self._process_triggers_loop())
    
    async def stop(self):
        """Stop the SOAR engine"""
        self.is_running = False
        logger.info("SOAR engine stopped")
    
    async def execute_playbook(self, db: Session, playbook_id: uuid.UUID, 
                           context: Dict[str, Any], triggered_by: Optional[uuid.UUID] = None) -> SOARExecutionLog:
        """Execute a SOAR playbook"""
        
        # Check concurrent execution limit
        if len(self.running_executions) >= self.max_concurrent:
            raise Exception(f"Maximum concurrent executions ({self.max_concurrent}) reached")
        
        # Get playbook
        playbook = db.query(SOARPlaybook).filter(
            SOARPlaybook.id == playbook_id,
            SOARPlaybook.is_active == True
        ).first()
        
        if not playbook:
            raise Exception(f"Playbook {playbook_id} not found or inactive")
        
        # Create execution log
        execution = SOARExecutionLog(
            playbook_id=playbook_id,
            alert_id=context.get("alert_id"),
            incident_id=context.get("incident_id"),
            status=ExecutionStatus.RUNNING,
            triggered_by=triggered_by,
            started_at=datetime.utcnow()
        )
        
        db.add(execution)
        db.commit()
        db.refresh(execution)
        
        # Add to running executions
        self.running_executions[execution.id] = execution
        
        try:
            # Get actions ordered by order_index
            actions = db.query(SOARAction).filter(
                SOARAction.playbook_id == playbook_id,
                SOARAction.is_active == True
            ).order_by(SOARAction.order_index).all()
            
            execution_results = []
            
            for action in actions:
                # Get action executor
                executor = self.action_executors.get(action.action_type)
                if not executor:
                    logger.warning(f"No executor found for action type: {action.action_type}")
                    continue
                
                try:
                    # Execute action with timeout
                    result = await asyncio.wait_for(
                        executor.execute(action, context),
                        timeout=action.timeout_seconds
                    )
                    
                    execution_results.append({
                        "action_id": str(action.id),
                        "action_type": action.action_type,
                        "result": result
                    })
                    
                    # Check if action failed and should stop execution
                    if result.get("status") == "failed":
                        logger.error(f"Action {action.name} failed: {result.get('error')}")
                        break
                
                except asyncio.TimeoutError:
                    logger.error(f"Action {action.name} timed out")
                    execution_results.append({
                        "action_id": str(action.id),
                        "action_type": action.action_type,
                        "result": {"status": "failed", "error": "Action timeout"}
                    })
                    break
                
                except Exception as e:
                    logger.error(f"Error executing action {action.name}: {e}")
                    execution_results.append({
                        "action_id": str(action.id),
                        "action_type": action.action_type,
                        "result": {"status": "failed", "error": str(e)}
                    })
                    break
            
            # Update execution log
            execution.status = ExecutionStatus.SUCCESS
            execution.completed_at = datetime.utcnow()
            execution.execution_details = execution_results
            db.commit()
            
            # Publish execution result
            await redis_client.publish(REDIS_CHANNELS["SOAR"], {
                "execution_id": str(execution.id),
                "playbook_id": str(playbook_id),
                "status": execution.status,
                "results": execution_results
            })
            
            logger.info(f"Playbook {playbook.name} executed successfully")
            
        except Exception as e:
            # Update execution log with error
            execution.status = ExecutionStatus.FAILED
            execution.completed_at = datetime.utcnow()
            execution.error_message = str(e)
            db.commit()
            
            logger.error(f"Playbook {playbook.name} execution failed: {e}")
        
        finally:
            # Remove from running executions
            if execution.id in self.running_executions:
                del self.running_executions[execution.id]
        
        return execution
    
    async def _process_triggers_loop(self):
        """Background loop to process triggers"""
        while self.is_running:
            try:
                # Subscribe to alerts channel
                pubsub = await redis_client.subscribe(REDIS_CHANNELS["ALERTS"])
                
                async for message in pubsub.listen():
                    if message["type"] == "message":
                        try:
                            alert_data = json.loads(message["data"])
                            await self._check_alert_triggers(alert_data)
                        except Exception as e:
                            logger.error(f"Error processing alert trigger: {e}")
                
            except Exception as e:
                logger.error(f"Error in SOAR trigger loop: {e}")
                await asyncio.sleep(5)  # Wait before retrying
    
    async def _check_alert_triggers(self, alert_data: Dict[str, Any]):
        """Check if alert triggers any playbooks"""
        from app.models.database import SessionLocal
        db = SessionLocal()
        
        try:
            # Get playbooks that trigger on alerts
            playbooks = db.query(SOARPlaybook).filter(
                SOARPlaybook.trigger_type == TriggerType.ALERT,
                SOARPlaybook.is_active == True
            ).all()
            
            for playbook in playbooks:
                if await self._evaluate_trigger_conditions(playbook.trigger_conditions, alert_data):
                    # Execute playbook
                    context = {
                        "alert_id": alert_data.get("id"),
                        "severity": alert_data.get("severity"),
                        "title": alert_data.get("title"),
                        "description": alert_data.get("description"),
                        "source_ip": alert_data.get("source_ip")
                    }
                    
                    logger.info(f"Triggering playbook {playbook.name} for alert {alert_data.get('id')}")
                    await self.execute_playbook(db, playbook.id, context)
        
        finally:
            db.close()
    
    async def _evaluate_trigger_conditions(self, conditions: Dict[str, Any], alert_data: Dict[str, Any]) -> bool:
        """Evaluate if trigger conditions match alert data"""
        if not conditions:
            return True
        
        # Example condition evaluation
        severity_match = True
        if "severity" in conditions:
            required_severities = conditions["severity"]
            alert_severity = alert_data.get("severity")
            severity_match = alert_severity in required_severities
        
        # Add more condition types as needed
        return severity_match

